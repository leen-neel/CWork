#include <stdio.h>

int main()
{
    // int
    int age = 69420;
    printf("Age of consent is %d\n", age);

    // string
    char ch = 'N';
    printf("%c\n", ch);

    // float
    float nice = 6.9;
    printf("%f\n", nice);

    // double
    double funny = 69420.24;
    printf("%lf\n", funny);

    return 0;
}
